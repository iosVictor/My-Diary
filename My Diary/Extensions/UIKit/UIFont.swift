import UIKit

extension UIFont {
    
    static func appleSDGothicNeo20() -> UIFont? {
        return UIFont.init(name: "Apple SD Gothic Neo", size: 20)
    }
    
    static func appleSDGothicNeo14() -> UIFont? {
        return UIFont.init(name: "Apple SD Gothic Neo", size: 14)
    }
    
    static func appleSDGothicNeoBold20() -> UIFont? {
        return UIFont.init(name: "Apple SD Gothic Neo Bold", size: 20)
    }
    
    static func appleSDGothicNeoBold14() -> UIFont? {
        return UIFont.init(name: "Apple SD Gothic Neo Bold", size: 14)
    }
}
